-- DATEDIFF (DATEPART, STARTDATE, ENDDATE)
-- RETORNA O NÚMERO DE LIMITES DE DATEPART DE DATA OU HORA ENTRE DUAS DATAS ESPECIFICADAS
SELECT DATEDIFF(DAY, '1984-03-05', '1991-03-01');
SELECT DATEDIFF(MONTH, '1984-03-05', '1991-03-01');
SELECT DATEDIFF(YEAR, '1984-03-05', '1991-03-01');

SELECT DATEDIFF(DAY, '1991-03-01', GETDATE());
SELECT DATEDIFF(MONTH, '1991-03-01', GETDATE());
SELECT DATEDIFF(YEAR, '1991-03-01', GETDATE());

